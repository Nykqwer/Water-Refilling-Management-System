    private void customer(){
 
      try{
          String sql = "INSERT INTO customer(id_number,customer_name,address,contact)VALUES(?,?,?,?)";
          pst = conn.prepareStatement(sql);
       
          pst.setString(1,id_number1.getText());
          pst.setString(2,c_name1.getText());
          pst.setString(3,address1.getText());
          pst.setString(4,contact1.getText());
          
          pst.execute();
          JOptionPane.showMessageDialog(null,"info added Successfully");
          
      }catch(Exception e){
  
          JOptionPane.showMessageDialog(null,e);
      }  
    } 
