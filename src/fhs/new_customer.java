private void get_new_id(){
        try{
            pst = conn.prepareStatement("SELECT MAX(id_number)+1 AS next_id FROM customer");
            rs = pst.executeQuery();
            if(rs.next()){
                double nextId = rs.getDouble("next_id");
                int roundedId = (int) nextId; // Convert to integer to remove decimal places
            id_number1.setText(Integer.toString(roundedId));
            }
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }
