    private void type() {
        type1.setText(type.getSelectedItem().toString());
        String selectedOption = (String) type.getSelectedItem();
        if (selectedOption.equals("Walk-in")) {
            int b = Integer.parseInt(quantity.getText());
            int a = Integer.parseInt(quantity1.getText());
            int amt = b + a;
            int amt1 = amt * 25;
            lblprice1.setText("" + amt1);

            lblquantity.setText(String.valueOf(amt));
            lblprice1.setText(String.valueOf(amt1));
          
            cart.setEnabled(false);
        } else if (selectedOption.equals("Delivery")) {
            int b = Integer.parseInt(quantity.getText());
            int a = Integer.parseInt(quantity1.getText());
            int amt = b + a;
            int amt1 = amt * 15;
         
            lblquantity.setText(String.valueOf(amt));
            lblprice1.setText(String.valueOf(amt1));
         
            cart.setEnabled(true);
        }

    }

