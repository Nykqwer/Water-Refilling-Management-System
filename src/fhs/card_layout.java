 private void update_inventory() {
        int slim;
        int round;
        if (quantity1.getText().isEmpty()) {
            slim = 0;
        } else  {
            slim = Integer.parseInt(quantity1.getText());
        }
        if (quantity.getText().isEmpty()) {
            round = 0;
        } else {
            round = Integer.parseInt(quantity.getText());
        }
        try {

            PreparedStatement ps1 = conn.prepareStatement("UPDATE inventory SET quantity = quantity - " + slim + " WHERE item_name = 'gallon-slim'");
            ps1.executeUpdate();

            PreparedStatement ps2 = conn.prepareStatement("UPDATE inventory SET quantity = quantity - " + round + " WHERE item_name = 'gallon-round'");
            ps2.executeUpdate();

           
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }
